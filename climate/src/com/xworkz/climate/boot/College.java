package com.xworkz.climate.boot;

public class College {
	public String clgName;
	public float noOfBranches;
	public boolean isItPrivateOrPublic;
	public String getClgName() {
		return clgName;
	}
	public float getNoOfBranches() {
		return noOfBranches;
	}
	public boolean isItPrivateOrPublic() {
		return isItPrivateOrPublic;
	}
	public void setClgName(String clgName) {
		this.clgName = clgName;
	}
	public void setNoOfBranches(float noOfBranches) {
		this.noOfBranches = noOfBranches;
	}
	public void setItPrivateOrPublic(boolean isItPrivateOrPublic) {
		this.isItPrivateOrPublic = isItPrivateOrPublic;
	}
	

}
