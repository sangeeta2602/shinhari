package com.xworkz.animals.boot;

public abstract class Monkey {
	
	public abstract void generation();
	public abstract void omnivorous();
	public abstract void walking();
	public abstract void sleeping();
	public abstract void running();
	public abstract void intelligence();
	public abstract void color();
	
	public void face() {
		System.out.println("face method");
		}
	public void hands() {
		System.out.println("hand method");
	}
	public void liveIn() {
		System.out.println("live in method ");
	}
	public void behaviour() {
		System.out.println("behaviour method");
	}
	

}
