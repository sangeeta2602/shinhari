package com.shinhari.medical.boot;

public class Medical {
     int noStaff;
     String nameOfHospital;
     float entryFees;
	public int getNoStaff() {
		return noStaff;
	}
	public void setNoStaff(int noStaff) {
		this.noStaff = noStaff;
	}
	public String getNameOfHospital() {
		return nameOfHospital;
	}
	public void setNameOfHospital(String nameOfHospital) {
		this.nameOfHospital = nameOfHospital;
	}
	public float getEntryFees() {
		return entryFees;
	}
	public void setEntryFees(float entryFees) {
		this.entryFees = entryFees;
	}
     
}
