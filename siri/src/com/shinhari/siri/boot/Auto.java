package com.shinhari.siri.boot;

public class Auto {
	int price;
    String name;
    float meterCharge;
    char wheeler;
    long contactNum;
    Boolean isItSafe;
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getMeterCharge() {
		return meterCharge;
	}
	public void setMeterCharge(float meterCharge) {
		this.meterCharge = meterCharge;
	}
	public char getWheeler() {
		return wheeler;
	}
	public void setWheeler(char wheeler) {
		this.wheeler = wheeler;
	}
	public long getContactNum() {
		return contactNum;
	}
	public void setContactNum(long contactNum) {
		this.contactNum = contactNum;
	}
	public Boolean getIsItSafe() {
		return isItSafe;
	}
	public void setIsItSafe(Boolean isItSafe) {
		this.isItSafe = isItSafe;
	}

}
